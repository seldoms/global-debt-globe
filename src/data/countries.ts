function composition(
  debt: number,
  primary: number,
  regional: number,
  social: number,
  other: number,
): DebtCompositionItem[] {
  return [
    {
      label: "中央/联邦政府债",
      share: primary,
      amountTrillionsUsd: Number(((debt * primary) / 100).toFixed(2)),
      note: "按可比展示口径估算，非所有国家均披露完全一致分类。",
    },
    {
      label: "地方/州/公共机构债",
      share: regional,
      amountTrillionsUsd: Number(((debt * regional) / 100).toFixed(2)),
      note: "联邦制国家占比通常更高，单一制国家多并入一般政府。",
    },
    {
      label: "社保/公共基金相关",
      share: social,
      amountTrillionsUsd: Number(((debt * social) / 100).toFixed(2)),
      note: "用于视觉拆分，跨国不可直接等同会计科目。",
    },
    {
      label: "其他显性政府负债",
      share: other,
      amountTrillionsUsd: Number(((debt * other) / 100).toFixed(2)),
      note: "包括口径差异、公共实体与统计调整项。",
    },
  ];
}

